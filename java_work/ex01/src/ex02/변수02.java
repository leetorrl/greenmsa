package ex02;

public class 변수02 {

	public static void main(String[] args) {
		
		int num1;
		
		num1=20+30;
	System.out.println(num1);
	
		int num2 = 50;
	
	
		int num3 = num1+num2;
		
		System.out.println(num3);
	
		System.out.println(num1+"+"+num2+"="+num3);
		//+는 나타낼 명령 붙일때 마다 달아줘야함
		
	}
	
}
