public class 변수03 {

	public static void main(String[] args) {
		
		
		double num1,num2; //double은 한번에 두개? 기정가능
		num1=1.000001;
		num2=2.000002;
		
		int _a1 = 10; //대소문자 구분됨 , 변수명은 키워드(void등)
		               // 및 명령어(int)로는 하지말것
		int A = 20; //int는 하나하나 일일이 지정가능
		
		System.out.println(num1+num2);
		System.out.println(A);
		System.out.println(_a1);
	}
	
	
}
