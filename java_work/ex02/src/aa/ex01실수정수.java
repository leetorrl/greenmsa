package aa;

public class ex01실수정수 {

	public static void main(String[] args) {
		System.out.println("연습중");

		int a=10; //int는 정수
		double b=5.4;  //double은 실수 소수점
		int c=3;
		
		
		System.out.println(a+b);
		System.out.println(a/b);
		System.out.println(a%b); //나머지를 구할땐 퍼센트를 쓸것
		System.out.println(a/c);
		System.out.println(a%c);
	
	}

}
